# Студент: ФИО на русском языке
student_name = "Ши Юэ"  # ФИО студента

# Номер группы
group_number = "3140801/52501"  # Номер группы

# Название объекта строительства
project_name = "回家"  # Название объекта

# Количество этажей
floors = 8  # Количество этажей

# Высота здания в метрах
height = 25.3  # Высота здания в метрах

# Тип здания: жилое / нежилое
is_residential = True  # Жилой / нежилой

# Год постройки
construction_year = 2003  # Год постройки

# Формирование словаря с данными объекта (паспорт здания)
building_passport = {
    "Составитель": student_name,          # Составитель паспорта
    "Группа": group_number,               # Номер группы студента
    "Объект": project_name,               # Наименование объекта
    "Этажность": f"{floors} этажей",      # Количество этажей с единицей измерения
    "Высота": f"{height} м",              # Высота здания с единицей измерения
    "Тип": "Жилой" if is_residential else "Нежилой",  # Тип здания
    "Год постройки": construction_year    # Год ввода в эксплуатацию
}

# Вывод паспорта строительного объекта в читаемом виде с заголовком
print("=== ПАСПОРТ СТРОИТЕЛЬНОГО ОБЪЕКТА ===")  # Заголовок
for key, value in building_passport.items():     # Перебор всех элементов словаря
    print(f"{key}: {value}")                     # Печать ключа и значения
print("=====================================")    # Завершающая разделительная линия

