w = {
    "Кирпич": {"q":5000, "p":12.5, "min":1000},
    "Цемент": {"q":120, "p":450, "min":50},
    "Песок": {"q":8, "p":800, "min":10},
    "Арматура": {"q":30, "p":48000, "min":20},
    "Бетон": {"q":45, "p":4200, "min":15}
}

for n, d in w.items():
    v = d["q"] * d["p"]
    c = "КРИТИЧ!" if d["q"] < d["min"] else ""
    print(f"{n:10} | {d['q']:5} | {d['p']:8} | {v:10} {c}")

# выдача
w["Цемент"]["q"] -= 25
print(f"Выдано 25 цемента, осталось {w['Цемент']['q']}")